num1= int(input("Enter first number: "))
num2= int(input("Enter second number: "))

add = num1+num2
subtract = num1-num2
multipy = num1*num2
divide = float(num1)/float(num2)

print(f"{num1}+{num2} is {add}")
print(f"{num1}-{num2} is {subtract}")
print(f"{num1}X{num2} is {multipy}")
print(f"{num1}/{num2} is {divide}")